        function renderBlogPosts() {
            const blogGrid = document.getElementById('blogGrid');
            let filteredPosts = currentPosts;

            if (currentFilter !== 'all') {
                filteredPosts = currentPosts.filter(post => post.category === currentFilter);
            }

            if (searchTerm) {
                filteredPosts = filteredPosts.filter(post => 
                    post.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
                    post.excerpt.toLowerCase().includes(searchTerm.toLowerCase())
                );
            }

            blogGrid.innerHTML = filteredPosts.map(createBlogCard).join('');
        }

        // Filter functionality
        document.querySelectorAll('.filter-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                currentFilter = btn.dataset.category;
                renderBlogPosts();
            });
        });

        // Search functionality
        document.getElementById('searchInput').addEventListener('input', (e) => {
            searchTerm = e.target.value;
            renderBlogPosts();
        });

        // Modal functionality
        function openArticle(postId) {
            const post = blogPosts.find(p => p.id === postId);
            if (!post) return;

            // Populate modal content
            document.getElementById('modalTitle').textContent = post.title;
            document.getElementById('modalCategory').textContent = post.category.replace('-', ' ');
            document.getElementById('modalReadTime').textContent = post.readTime;
            document.getElementById('modalAuthor').textContent = post.author;
            document.getElementById('modalDate').textContent = post.date;
            document.getElementById('modalContent').innerHTML = post.fullContent;
            
            // Set modal image
            const modalImage = document.getElementById('modalImage');
            modalImage.src = post.image;
            modalImage.alt = post.title;
            modalImage.style.display = 'block';
            modalImage.nextElementSibling.style.display = 'none';
            
            // Generate author initials
            const initials = post.author.split(' ').map(name => name[0]).join('').toUpperCase();
            document.getElementById('modalAuthorInitials').textContent = initials;

            // Show modal using Bootstrap
            const modal = new bootstrap.Modal(document.getElementById('articleModal'));
            modal.show();
        }

